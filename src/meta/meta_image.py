'''Describes an image to be used on game.'''

#------------------------------------------------------------------------------#
class MetaImage:

    #--------------------------------------------------------------------------#
    def __init__(self, size=None, color=(0,0,0), path=None):
        '''Create a MetaImage'''

        self.size  = size  # (width, height)
        self.color = color # (R,G,B)
        self.path  = path

